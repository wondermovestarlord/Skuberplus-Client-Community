#!/usr/bin/env node

/**
 * Purpose: Patch node-pty build files for Windows.
 * - Fix winpty.gyp paths to use absolute paths.
 * - Remove SpectreMitigation settings.
 * - Generate GenVersion.h.
 */

const fs = require("fs");
const path = require("path");
const { execSync } = require("child_process");

const projectRoot = path.join(__dirname, "..");
const nodePtyRoot = path.join(projectRoot, "node_modules", "node-pty");

function fail(message) {
  console.error(message);
  process.exit(1);
}

if (!fs.existsSync(nodePtyRoot)) {
  fail("node-pty is not installed. Run: pnpm install --ignore-scripts");
}

console.log("Patching node-pty for Windows");

const winptyGyp = path.join(nodePtyRoot, "deps", "winpty", "src", "winpty.gyp");
if (!fs.existsSync(winptyGyp)) {
  fail(`Missing ${winptyGyp}`);
}

let winptyContent = fs.readFileSync(winptyGyp, "utf8");
const sharedPath = path.join(nodePtyRoot, "deps", "winpty", "src", "shared").replace(/\\/g, "\\\\");

winptyContent = winptyContent.replace(
  /'WINPTY_COMMIT_HASH%': '<!\(cmd \/c "cd shared && GetCommitHash\.bat"\)',/,
  `'WINPTY_COMMIT_HASH%': '<!(cmd /c "${sharedPath}\\\\GetCommitHash.bat")',`,
);

const includePattern = "'include_dirs': [";
const includeIndex = winptyContent.indexOf(includePattern);
if (includeIndex !== -1) {
  const blockEnd = winptyContent.indexOf("]", includeIndex);
  if (blockEnd !== -1) {
    const originalBlock = winptyContent.substring(includeIndex, blockEnd + 1);
    if (originalBlock.includes("UpdateGenVersion.bat")) {
      const newBlock = `'include_dirs': [
            # GenVersion.h is pre-generated by fix-node-pty-windows.js
            'gen',
        ]`;
      winptyContent = winptyContent.substring(0, includeIndex) + newBlock + winptyContent.substring(blockEnd + 1);
    }
  }
}

winptyContent = winptyContent.replace(
  /'msvs_configuration_attributes':\s*\{[^}]*'SpectreMitigation':\s*'Spectre'[^}]*\}/g,
  "'msvs_configuration_attributes': {\n        }",
);

fs.writeFileSync(winptyGyp, winptyContent, "utf8");

const bindingGyp = path.join(nodePtyRoot, "binding.gyp");
if (!fs.existsSync(bindingGyp)) {
  fail(`Missing ${bindingGyp}`);
}

let bindingContent = fs.readFileSync(bindingGyp, "utf8");
bindingContent = bindingContent.replace(
  /'msvs_configuration_attributes':\s*\{[^}]*'SpectreMitigation':\s*'Spectre'[^}]*\}/g,
  "'msvs_configuration_attributes': {\n        }",
);
fs.writeFileSync(bindingGyp, bindingContent, "utf8");

const genDir = path.join(nodePtyRoot, "deps", "winpty", "src", "gen");
if (!fs.existsSync(genDir)) {
  fs.mkdirSync(genDir, { recursive: true });
}

let commitHash;
try {
  commitHash = execSync("git log -1 --format=%H", {
    cwd: projectRoot,
    encoding: "utf8",
  }).trim();
} catch (error) {
  commitHash = "unknown";
}

const versionFile = path.join(nodePtyRoot, "deps", "winpty", "VERSION.txt");
const version = fs.existsSync(versionFile) ? fs.readFileSync(versionFile, "utf8").trim() : "0.4.3";

const genVersionH = path.join(genDir, "GenVersion.h");
const genVersionContent = `// AUTO-GENERATED BY fix-node-pty-windows.js
const char GenVersion_Version[] = "${version}";
const char GenVersion_Commit[] = "${commitHash}";
`;

fs.writeFileSync(genVersionH, genVersionContent, "utf8");

console.log("node-pty patch complete");
console.log("Next:");
console.log("  cd node_modules\\node-pty");
console.log("  pnpm dlx node-gyp rebuild --target=35.7.5 --arch=x64 --dist-url=https://electronjs.org/headers");
